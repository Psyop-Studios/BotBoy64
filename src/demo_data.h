// ============================================================
// DEMO LIST - Register all available demos here
// ============================================================

#define DEMO_COUNT 3  // Update this when you add more demos

// Demo index constants for tutorial reference
// #define DEMO_IDX_ARC_JUMP_TUTORIAL - removed (arc jump tutorial demo no longer in list)

// DEMO_LIST is defined in demo_scene.c to ensure only ONE copy exists
// (static arrays in headers create duplicate copies per compilation unit!)
extern const DemoData DEMO_LIST[DEMO_COUNT];

// ============================================================
// DEMO PLAYBACK FUNCTIONS
// ============================================================

static inline int demo_get_count(void) {
    return DEMO_COUNT;
}

static inline const DemoData* demo_get(int index) {
    if (index < 0 || index >= DEMO_COUNT) return NULL;
    return &DEMO_LIST[index];
}

static inline const DemoData* demo_get_random(void) {
    if (DEMO_COUNT == 0) return NULL;
    // Simple random using TICKS
    int index = (int)(TICKS_READ() % DEMO_COUNT);
    return &DEMO_LIST[index];
}

// Start demo playback from ROM data
static inline bool demo_start_playback(const DemoData* demo) {
    if (!demo || demo->frameCount == 0 || !demo->frames) return false;

    g_demoState.playing = true;
    g_demoState.currentFrame = 0;
    g_demoState.activeDemo = demo;
    return true;
}

// Stop demo playback
static inline void demo_stop_playback(void) {
    g_demoState.playing = false;
    g_demoState.currentFrame = 0;
    g_demoState.activeDemo = NULL;
}

// Get current frame data, returns false when demo is complete
static inline bool demo_get_frame(int8_t* stickX, int8_t* stickY, uint16_t* buttons) {
    if (!g_demoState.playing || !g_demoState.activeDemo) return false;

    // Validate activeDemo pointer is within DEMO_LIST range
    const DemoData* listStart = &DEMO_LIST[0];
    const DemoData* listEnd = &DEMO_LIST[DEMO_COUNT];
    if (g_demoState.activeDemo < listStart || g_demoState.activeDemo >= listEnd) {
        debugf("ERROR: demo_get_frame: activeDemo 0x%08x out of range\n",
               (uint32_t)g_demoState.activeDemo);
        demo_stop_playback();
        return false;
    }

    // Validate frames pointer
    if (!g_demoState.activeDemo->frames) {
        debugf("ERROR: demo_get_frame: frames pointer is NULL\n");
        demo_stop_playback();
        return false;
    }

    if (g_demoState.currentFrame >= g_demoState.activeDemo->frameCount) {
        // Demo complete
        demo_stop_playback();
        return false;
    }

    // Validate currentFrame is reasonable
    if (g_demoState.currentFrame < 0) {
        debugf("ERROR: demo_get_frame: currentFrame %d is negative\n", g_demoState.currentFrame);
        demo_stop_playback();
        return false;
    }

    const ReplayFrame* frame = &g_demoState.activeDemo->frames[g_demoState.currentFrame];
    *stickX = frame->stickX;
    *stickY = frame->stickY;
    *buttons = frame->buttons;

    g_demoState.currentFrame++;
    return true;
}

// Check if demo is currently playing
static inline bool demo_is_playing(void) {
    return g_demoState.playing;
}

// Get the start position for the active demo (for demos starting mid-level)
// Returns true if demo has a custom start position (non-zero), false otherwise
static inline bool demo_get_start_position(float* outX, float* outY, float* outZ) {
    if (!g_demoState.activeDemo) {
        *outX = 0; *outY = 0; *outZ = 0;
        return false;
    }

    // Validate activeDemo pointer is within DEMO_LIST range
    const DemoData* listStart = &DEMO_LIST[0];
    const DemoData* listEnd = &DEMO_LIST[DEMO_COUNT];
    if (g_demoState.activeDemo < listStart || g_demoState.activeDemo >= listEnd) {
        debugf("ERROR: activeDemo pointer 0x%08x out of range [0x%08x, 0x%08x)\n",
               (uint32_t)g_demoState.activeDemo, (uint32_t)listStart, (uint32_t)listEnd);
        *outX = 0; *outY = 0; *outZ = 0;
        return false;
    }

    // Read values as raw bits first to avoid FPU exceptions on denormals
    // Use memcpy to avoid strict-aliasing violations
    uint32_t rawX, rawY, rawZ;
    memcpy(&rawX, &g_demoState.activeDemo->startX, sizeof(uint32_t));
    memcpy(&rawY, &g_demoState.activeDemo->startY, sizeof(uint32_t));
    memcpy(&rawZ, &g_demoState.activeDemo->startZ, sizeof(uint32_t));

    // Check for denormal/garbage values (exponent field = 0 but mantissa != 0)
    // IEEE 754: bits 30-23 are exponent for single precision
    uint32_t expX = (rawX >> 23) & 0xFF;
    uint32_t expY = (rawY >> 23) & 0xFF;
    uint32_t expZ = (rawZ >> 23) & 0xFF;
    uint32_t mantX = rawX & 0x7FFFFF;
    uint32_t mantY = rawY & 0x7FFFFF;
    uint32_t mantZ = rawZ & 0x7FFFFF;

    // Denormal if exponent is 0 but mantissa is non-zero
    bool isDenormal = (expX == 0 && mantX != 0) ||
                      (expY == 0 && mantY != 0) ||
                      (expZ == 0 && mantZ != 0);

    if (isDenormal) {
        debugf("ERROR: Demo start position contains denormal (raw: 0x%08x, 0x%08x, 0x%08x)\n",
               rawX, rawY, rawZ);
        *outX = 0; *outY = 0; *outZ = 0;
        return false;
    }

    // Values are safe to use as floats
    *outX = g_demoState.activeDemo->startX;
    *outY = g_demoState.activeDemo->startY;
    *outZ = g_demoState.activeDemo->startZ;

    // Return true if at least one coordinate is non-zero (custom position)
    return (rawX != 0 || rawY != 0 || rawZ != 0);
}

